class Solution{
    public static ArrayList<Integer> mergeKArrays(int[][] arrays,int k) 
    {
        PriorityQueue<Integer> queue = new PriorityQueue<>();
        for(int i=0;i<k;i++)
        {
            for(int j=0;j<k;j++)
            {
                queue.add(arrays[i][j]);
            }
        }
        ArrayList<Integer> out = new ArrayList<>();
        for(int i=0;i<k;i++)
        {
            for(int j=0;j<k;j++)
            {
                out.add(queue.peek());
                queue.poll();
            }
        }
        return out;
    }
}
